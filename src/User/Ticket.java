    private void saveData() {
        String ticketNo = jTextField1.getText();
        String timeIn = jTextField2.getText();
        String timeOut = jTextField3.getText();
        String feeText = fees.getText().replace(" Peso", "");
        String status = (String) statusComboBox.getSelectedItem();

        String query = "INSERT INTO tickettimer (TicketNo, TimeIn, TimeOut, Fee, Status) " +
                       "VALUES ('" + ticketNo + "', '" + timeIn + "', '" + timeOut + "', '" + feeText + "', '" + status + "') " +
                       "ON DUPLICATE KEY UPDATE TimeIn = VALUES(TimeIn), TimeOut = VALUES(TimeOut), Fee = VALUES(Fee), Status = VALUES(Status)";

        dcConnector dbc = new dcConnector();
        boolean success = dbc.insertData(query);

        if (success) {
            JOptionPane.showMessageDialog(null, "Data saved successfully!");
        } else {
            JOptionPane.showMessageDialog(null, "Error saving data.");
        }
    }
    
    
    private void JoutTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JoutTableMouseClicked
        int selectedRow = JoutTable.getSelectedRow();
        if (selectedRow != -1) {
            TableModel model = JoutTable.getModel();
            String ticketNo = model.getValueAt(selectedRow, 1).toString();
            String timeIn = model.getValueAt(selectedRow, 2).toString();
            String timeOut = model.getValueAt(selectedRow, 3).toString();

            jTextField1.setText(ticketNo);
            jTextField2.setText(timeIn);
            jTextField3.setText(timeOut);

            // Calculate fee based on TimeIn and TimeOut
            try {
                Date dateIn = parseTime(timeIn);
                Date dateOut = parseTime(timeOut);

                // Handle case where timeOut is past midnight
                if (dateOut.before(dateIn)) {
                    dateOut = new Date(dateOut.getTime() + 24 * 60 * 60 * 1000);
                }

                long diff = dateOut.getTime() - dateIn.getTime();
                long diffMinutes = diff / (60 * 1000);
                long hours = diffMinutes / 60;
                long minutes = diffMinutes % 60;

                // Calculate fee: 5 pesos per hour
                double fee = (hours + (minutes / 60.0)) * 5;
                fees.setText(String.format("%.2f Peso", fee));
            } catch (ParseException ex) {
                ex.printStackTrace();
                fees.setText("0.00 Peso");
            }

            // Set status to "Paid" for demonstration purposes
            statusComboBox.setSelectedItem("Paid");
        }
    }//GEN-LAST:event_JoutTableMouseClicked

    
    private Date parseTime(String time) throws ParseException {
    SimpleDateFormat sdf12Hour = new SimpleDateFormat("hh:mm a");
    SimpleDateFormat sdf24Hour = new SimpleDateFormat("HH:mm");

    try {
        return sdf12Hour.parse(time);
    } catch (ParseException e) {
        return sdf24Hour.parse(time);
    }
}
    
    
    
