    private void printButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printButtonActionPerformed
                                             
        try {
        // Save the current data first
        String ticketNo = jTextField1.getText();
        String timeIn = jTextField2.getText();
        String timeOut = jTextField3.getText();
        String feeText = fees.getText().replace(" Peso", "");
        String status = (String) statusComboBox.getSelectedItem();

        String query = "INSERT INTO tickettimer (TicketNo, TimeIn, TimeOut, Fee, Status) " +
                       "VALUES ('" + ticketNo + "', '" + timeIn + "', '" + timeOut + "', '" + feeText + "', '" + status + "') " +
                       "ON DUPLICATE KEY UPDATE TimeIn = VALUES(TimeIn), TimeOut = VALUES(TimeOut), Fee = VALUES(Fee), Status = VALUES(Status)";

        dcConnector dbc = new dcConnector();
        boolean success = dbc.insertData(query);

        if (!success) {
            JOptionPane.showMessageDialog(null, "Error saving data.");
            return; // Exit if saving data failed
        }

        // Create a new instance of your database connector
        dcConnector dc = new dcConnector();

        // Query the database for the ticket details
        ResultSet rs = dc.getData("SELECT * FROM tickettimer WHERE TicketNo = " + ticketNo);

        if (rs.next()) {
          
            PrintTicket ipt = new PrintTicket();
            ipt.jTextField1.setText("" + rs.getInt("TicketNo"));
            ipt.jTextField2.setText("" + rs.getString("TimeIn"));
            ipt.jTextField3.setText("" + rs.getString("TimeOut"));
            ipt.fees.setText("" + rs.getString("Fee"));
            ipt.statusComboBox.setText("" + rs.getString("Status"));//setSelectedItem(rs.getString("Status")); 
            
            ipt.setVisible(true);

            
            this.dispose();
        } else {
        
            JOptionPane.showMessageDialog(this, "Ticket not found");
        }
    } catch (SQLException ex) {
        System.out.println("" + ex);
    } catch (NumberFormatException ex) {
        System.out.println("Invalid ticket number");
    }
    }//GEN-LAST:event_printButtonActionPerformed

