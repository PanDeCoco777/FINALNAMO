    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRow = VDB.getSelectedRow();
        if (selectedRow != -1) {
            // Confirm the deletion
            int confirmation = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this entry?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
            if (confirmation == JOptionPane.YES_OPTION) {
                try {
                    // Get the unique identifier from the selected row
                    String id = VDB.getValueAt(selectedRow, 0).toString();

                    // Remove the row from the table model
                    DefaultTableModel model = (DefaultTableModel) VDB.getModel();
                    model.removeRow(selectedRow);

                    // Connect to the database
                    Connection connect = DriverManager.getConnection("jdbc:mysql://localhost:3306/userandadmin", "root", "");
                    String query = "DELETE FROM parking WHERE ID = ?";
                    PreparedStatement pst = connect.prepareStatement(query);
                    pst.setString(1, id);

                    // Execute the delete statement
                    int rowsAffected = pst.executeUpdate();

                    // Check if the deletion was successful
                    if (rowsAffected > 0) {
                        JOptionPane.showMessageDialog(null, "Entry deleted successfully!");
                    } else {
                        JOptionPane.showMessageDialog(null, "Error: Entry not found in the database.");
                    }

                    // Close the resources
                    pst.close();
                    connect.close();
                } catch (SQLException ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(null, "Error deleting entry from the database.");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row to delete.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

