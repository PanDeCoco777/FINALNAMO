    private void V_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_V_addActionPerformed
String Tt = V_tt.getText();   //1
    String Cn = V_Cn.getText();
    String Br = V_b.getText();
    String Pn = V_pn.getText();
    String At = V_at.getText();
    String TO = V_TO.getText(); // New field for Timeout
    String C = V_Cc.getText();
    String Vt = V_vt.getSelectedItem().toString();

    // Check if all fields are filled
    if (Tt.isEmpty() || Cn.isEmpty() || Br.isEmpty() || Pn.isEmpty() || At.isEmpty() || TO.isEmpty() || C.isEmpty() || Vt.isEmpty() || jDateChooser1.getDate() == null) {
        JOptionPane.showMessageDialog(null, "Please fill in all the details.");
        return;
    }

    // Get the selected date from jDateChooser1 and format it
    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
    String selectedDate = sdf.format(jDateChooser1.getDate());

    try {
        Connection connect = DriverManager.getConnection("jdbc:mysql://localhost:3306/userandadmin", "root", "");

        // Check if the car (PlateNo) already exists in the database
        String checkQuery = "SELECT COUNT(*) FROM parking WHERE PlateNo = ?";
        PreparedStatement checkPst = connect.prepareStatement(checkQuery);
        checkPst.setString(1, Pn);
        ResultSet rs = checkPst.executeQuery();
        if (rs.next() && rs.getInt(1) > 0) {
            JOptionPane.showMessageDialog(null, "Car with PlateNo " + Pn + " already exists! ");
            return;
        }

        // Insert data into the database
        String query = "INSERT INTO parking (TicketNo, Companyname, Brand, PlateNo, TimeIn, Timeout, Color, VehicleType, Date) VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement pst = connect.prepareStatement(query, Statement.RETURN_GENERATED_KEYS);
        pst.setString(1, Tt);
        pst.setString(2, Cn);
        pst.setString(3, Br);
        pst.setString(4, Pn);
        pst.setString(5, At);
        pst.setString(6, TO); // Add Timeout
        pst.setString(7, C);
        pst.setString(8, Vt);
        pst.setString(9, selectedDate); // Set the selected date
        pst.executeUpdate();

        // Get the auto-generated ID
        ResultSet generatedKeys = pst.getGeneratedKeys();
        if (generatedKeys.next()) {
            int id = generatedKeys.getInt(1);
            // Update the table with the new entry
            DefaultTableModel model = (DefaultTableModel) VDB.getModel();
            model.addRow(new Object[]{id, Tt, Cn, Br, Pn, At, TO, C, Vt, selectedDate}); // Ensure the correct order of values
        }
        JOptionPane.showMessageDialog(null, "Details added successfully!");
    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error adding details to the database.");
    }

    }//GEN-LAST:event_V_addActionPerformed

