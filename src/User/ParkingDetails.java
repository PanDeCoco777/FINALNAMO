     public void table() {
     int rowIndex = VDB.getSelectedRow();
        if (rowIndex != -1) { // Check if a row is selected
            try {
                dcConnector dc = new dcConnector();
                TableModel tbl = VDB.getModel();
                // Assuming you have a way to identify the selected row uniquely, e.g., by TicketNo
                String selectedTicketNo = tbl.getValueAt(rowIndex, 1).toString(); // Assuming TicketNo is at index 1

                ResultSet Rs = dc.getData("SELECT TicketNo, Companyname, PlateNo, TimeIn, TimeOut, Date, Color, Brand, VehicleType FROM parking WHERE TicketNo = " + selectedTicketNo);

                DefaultTableModel df = (DefaultTableModel) VDB.getModel();
                df.setRowCount(0); // Clear existing rows

                while (Rs.next()) {
                    Vector<String> row = new Vector<>();
                    row.add(String.valueOf(Rs.getInt("TicketNo")));
                    row.add(Rs.getString("Companyname"));
                    row.add(Rs.getString("PlateNo"));
                    row.add(Rs.getString("TimeIn"));
                    row.add(Rs.getString("Date"));
                    row.add(Rs.getString("TimeOut"));
                    row.add(Rs.getString("Color"));
                    row.add(Rs.getString("Brand"));
                    row.add(Rs.getString("VehicleType"));
                    df.addRow(row);
                }
                Rs.close();
            } catch (SQLException ex) {
                System.out.println("Errors: " + ex.getMessage());
            }
        } else {
            // Handle the case when no row is selected
            System.out.println("No row is selected.");
        }
   } 
     
    
    
